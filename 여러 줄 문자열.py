# 여러 줄 문자열

multiline = '''Life is too short,
You need Python.'''
print(multiline)

multiline = """Life is too short,
You need Python."""
print(multiline)

multiline = '''
Life is too short,
You need Python.
'''
print(multiline)

multiline = """\
Life is too short,
You need Python.\
"""
print(multiline)


